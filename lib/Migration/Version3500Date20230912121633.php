namespace OCA\Mail\Migration;

use Closure;
use OCP\DB\ISchemaWrapper;
use OCP\DB\Types;
use OCP\Migration\IOutput;
use OCP\Migration\SimpleMigrationStep;

class Version3500Date20230912121633 extends SimpleMigrationStep {
	/**
	 * @param IOutput $output
	 * @param Closure(): ISchemaWrapper $schemaClosure
	 * @param array $options
	 * @return null|ISchemaWrapper
	 */
	public function changeSchema(IOutput $output, Closure $schemaClosure, array $options): ?ISchemaWrapper {
		/** @var ISchemaWrapper $schema */
		$schema = $schemaClosure();

		$mailboxesTable = $schema->getTable('mail_mailboxes');
		if (!$mailboxesTable->hasColumn('sync_lock')) {
			$mailboxesTable->addColumn('sync_lock', Types::INTEGER, [
				'length' => 4,
				'notnull' => false,
				'default' => null,
			]);
		}
		if (!$mailboxesTable->hasColumn('sync_token')) {
			$mailboxesTable->addColumn('sync_token', Types::STRING, [
				'length' => 255,
				'notnull' => false,
				'default' => null,
			]);
		}

		return $schema;
	}
}
